#!/usr/bin/env python3
"""Denoise ontology eval seed for cleaner local metrics.

Writes:
  - ontology/examples/eval_seed_clean.yaml  (NC+C only; drop borderline + low-quality stubs)
  - ontology/examples/denoise_report.yaml   (what changed and why)

Does NOT modify the 44-precedent file. Does not rewrite original eval_seed.yaml
(keep full set for authoring); loaders can opt into the clean file.

Usage:
  python ontology/tools/denoise_eval_seed.py
"""

from __future__ import annotations

import os
import re
from collections import Counter

import yaml

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
SEED = os.path.join(ROOT, "examples", "eval_seed.yaml")
OUT_CLEAN = os.path.join(ROOT, "examples", "eval_seed_clean.yaml")
OUT_REPORT = os.path.join(ROOT, "examples", "denoise_report.yaml")

# Placeholder / stub creatives that are not usable as gold text for lexical eval
_STUB_RE = re.compile(
    r"^\[(image|video|audio|landing[_\s-]?page)\]\s*$",
    re.IGNORECASE,
)


def _is_low_quality(ex: dict) -> str | None:
    content = (ex.get("content") or "").strip()
    if len(content) < 12:
        return "too_short"
    if _STUB_RE.match(content):
        return "modality_stub_only"
    # Pure bracket modality tags with almost no claim language
    if content.startswith("[") and len(content) < 40 and "—" not in content and "-" not in content[1:]:
        if content.count(" ") < 4:
            return "thin_placeholder"
    return None


def main() -> int:
    with open(SEED) as f:
        doc = yaml.safe_load(f) or {}
    examples = list(doc.get("examples") or [])

    kept: list[dict] = []
    dropped: list[dict] = []
    reasons: Counter[str] = Counter()

    for ex in examples:
        label = ex.get("label")
        if label == "borderline":
            reasons["drop_borderline"] += 1
            dropped.append({"id": ex.get("id"), "reason": "borderline", "label": label})
            continue
        if label not in ("non_compliant", "compliant"):
            reasons["drop_unknown_label"] += 1
            dropped.append({"id": ex.get("id"), "reason": "unknown_label", "label": label})
            continue
        lq = _is_low_quality(ex)
        if lq:
            reasons[f"drop_{lq}"] += 1
            dropped.append({"id": ex.get("id"), "reason": lq, "label": label})
            continue
        kept.append(ex)
        reasons["kept"] += 1

    report = {
        "source": "eval_seed.yaml",
        "input_count": len(examples),
        "kept_count": len(kept),
        "dropped_count": len(dropped),
        "reason_counts": dict(reasons),
        "label_counts_kept": dict(Counter(e.get("label") for e in kept)),
        "note": (
            "Clean set drops borderline and low-quality stubs. "
            "Precedents remain in eval_precedents.yaml and should always be loaded with this file. "
            "Original eval_seed.yaml is unchanged."
        ),
        "dropped_ids_sample": dropped[:40],
    }

    with open(OUT_CLEAN, "w") as f:
        f.write(
            "# Denoised evaluation seed — non_compliant + compliant only.\n"
            "# Generated by ontology/tools/denoise_eval_seed.py\n"
            "# Pair with eval_precedents.yaml for full clean eval.\n"
            "# Original noisy seed: eval_seed.yaml (unchanged).\n\n"
        )
        yaml.safe_dump({"examples": kept}, f, sort_keys=False, allow_unicode=True)

    with open(OUT_REPORT, "w") as f:
        yaml.safe_dump(report, f, sort_keys=False, allow_unicode=True)

    print(f"kept={len(kept)}/{len(examples)}  dropped={len(dropped)}")
    print(f"reasons={dict(reasons)}")
    print(f"wrote {OUT_CLEAN}")
    print(f"wrote {OUT_REPORT}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
